<h1>Decrement Operator</h1>
<h3>Pre-increment ++$x</h3>
<?php
// Pre-increment
// Menambah $x satu per satu, lalu mengembalikan $x
$x = 10;  
echo ++$x;
?>  

<h3>Post-increment $x++</h3>
<?php
// Post-increment
// Mengembalikan $x, lalu menambah $x satu per satu
$x = 10;  
echo $x++;
?>  

<h3>Pre-decrement --$x</h3>
<?php
// Pre-decrement
// Mengurangi $x satu per satu, lalu mengembalikan $x
$x = 10;  
echo --$x;
?>

<h3>Post-decrement $x--</h3>
<?php
// Post-decrement
// Mengembalikan $x, lalu mengurangi $x satu per satu
$x = 10;  
echo $x--;
?>

